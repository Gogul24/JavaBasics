package Arraylist;

public class Person {

		String name,gender;
		int age;
		
		public  Person(String name,String gender,int age) {
			this.age=age;
			this.gender=gender;
			this.name=name;
		}
		
		public int getAge() {
			return age;
		}
		public void setAge() {
			 this.age=age;
		}
		public String getName() {
			return name;
		}
		public void  setName() {
			this.name=name;
		}
		public String getGender() {
			return gender;
		}
		public void setGender() {
			this.gender=gender;
		}
		
	
}
